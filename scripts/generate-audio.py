def generate_audio():
    os.makedirs(AUDIO_DIR, exist_ok=True)
    total = 0
    total_size = 0
    
    for story_id, pages in STORIES.items():
        print(f"\n📖 Generating audio for: {story_id}")
        for i, text in enumerate(pages):
            filename = f"{story_id}_{i+1:02d}.mp3"
            filepath = os.path.join(AUDIO_DIR, filename)
            wav_path = filepath.replace('.mp3', '.wav')
            
            if os.path.exists(filepath):
                size = os.path.getsize(filepath)
                print(f"  ✓ Page {i+1}: exists ({size//1024}KB)")
                total += 1
                total_size += size
                continue
            
            try:
                # Generate WAV with espeak-ng
                # -v ru = Russian voice
                # -s 130 = speed (slower for storytelling)
                # -p 45 = pitch (slightly lower for warmth)
                # -a 180 = amplitude
                subprocess.run([
                    'espeak-ng', '-v', 'ru', '-s', '130', '-p', '45', '-a', '180',
                    '-w', wav_path, text
                ], check=True, capture_output=True)
                
                # Convert WAV to MP3 with ffmpeg
                subprocess.run([
                    'ffmpeg', '-y', '-i', wav_path, '-codec:a', 'libmp3lame',
                    '-b:a', '48k', filepath
                ], check=True, capture_output=True)
                
                # Remove WAV
                os.remove(wav_path)
                
                size = os.path.getsize(filepath)
                print(f"  ✓ Page {i+1}: {filename} ({size//1024}KB)")
                total += 1
                total_size += size
            except Exception as e:
                print(f"  ✗ Page {i+1}: FAILED - {e}")
    
    print(f"\n✅ Generated {total} audio files")
    print(f"📦 Total audio size: {total_size // 1024}KB ({total_size / (1024*1024):.1f}MB)")

if __name__ == "__main__":
    generate_audio()
